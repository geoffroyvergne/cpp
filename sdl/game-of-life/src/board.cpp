BoardSize Board::initTable() {
    std::vector<std::vector<int>> initBoardData = setPattern();

    int caseNumber = 0;

    for (int i = 0; i < initBoardData.size(); i++) {
        std::vector<Cell> v1;
        for (int j = 0; j < initBoardData[i].size(); j++) {            
            Cell* cell = new Cell();
            cell->state = initBoardData[i][j];
            cell->position = { caseNumber, j, i };

            v1.push_back(*cell);

            caseNumber++;
        }

        boardData.push_back(v1);
    }

    return { static_cast<int>(initBoardData[0].size()), static_cast<int>(initBoardData.size()) };
}

std::vector<std::vector<int>> Board::initRandomTable() {
    int x = 300;
    int y = 100;

    std::vector<std::vector<int>> randomTable;

    for (int i = 0; i < y; i++) {
        std::vector<int> v1;
        for (int j = 0; j < x; j++) {     
            v1.push_back(rand()%2);

        }
        randomTable.push_back(v1);
    }

    return randomTable;
}

